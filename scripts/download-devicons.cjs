// Downloads devicon SVGs and saves them as inline data for the icon mapping.
// Run: node scripts/download-devicons.cjs

const fs = require('fs');
const path = require('path');

const icons = [
  { slug: 'amazonwebservices', variant: 'original-wordmark' },
  { slug: 'azure', variant: 'original' },
  { slug: 'csharp', variant: 'original' },
  { slug: 'playwright', variant: 'original' },
  { slug: 'dynamodb', variant: 'original' },
  { slug: 'css3', variant: 'original' },
  { slug: 'sonarqube', variant: 'original' },
  { slug: 'grpc', variant: 'original' },
];

async function main() {
  const results = {};
  for (const { slug, variant } of icons) {
    const url = `https://cdn.jsdelivr.net/gh/devicons/devicon@latest/icons/${slug}/${slug}-${variant}.svg`;
    const res = await fetch(url);
    if (!res.ok) { console.error(`FAIL: ${slug}`); continue; }
    const svg = await res.text();
    results[slug] = svg.trim();
    console.log(`OK: ${slug} (${svg.length} chars)`);
  }
  
  const output = `// Auto-generated by scripts/download-devicons.cjs
// These are inlined SVGs from devicon to avoid runtime CDN dependency.

export const deviconSvgs: Record<string, string> = ${JSON.stringify(results, null, 2)};
`;
  
  fs.writeFileSync(path.join(__dirname, '..', 'skills', 'devicon-svgs.ts'), output);
  console.log('Written to skills/devicon-svgs.ts');
}

main();
